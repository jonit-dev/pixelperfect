function getIconForFeature(title: string): ReactElement {
  const lowerTitle = title.toLowerCase();
  if (lowerTitle.includes('text') || lowerTitle.includes('logo')) return featureIcons.text;
  if (
    lowerTitle.includes('fast') ||
    lowerTitle.includes('speed') ||
    lowerTitle.includes('lightning')
  )
    return featureIcons.speed;
  if (
    lowerTitle.includes('upscal') ||
    lowerTitle.includes('8x') ||
    lowerTitle.includes('4x') ||
    lowerTitle.includes('quality')
  )
    return featureIcons.quality;
  if (lowerTitle.includes('format') || lowerTitle.includes('support')) return featureIcons.format;
  if (lowerTitle.includes('batch') || lowerTitle.includes('multiple')) return featureIcons.batch;
  if (lowerTitle.includes('watermark') || lowerTitle.includes('clean'))
    return featureIcons.watermark;
  return featureIcons.default;
}

export function FeatureCard({ feature }: IFeatureCardProps): ReactElement {
  const icon = feature.icon ? (
    <span className="text-2xl">{feature.icon}</span>
  ) : (
    getIconForFeature(feature.title)
  );

  return (
    <div className="group relative glass-card animated-border h-full">
      <div className="relative">
        <div className="w-14 h-14 rounded-xl flex items-center justify-center mb-6 text-white shadow-md bg-accent glow-blue">
          {icon}
        </div>
        <h3 className="text-xl font-bold mb-3 text-white group-hover:text-accent transition-colors">
          {feature.title}
        </h3>
        <p className="text-muted-foreground text-base leading-relaxed">{feature.description}</p>
      </div>
    </div>
  );
}
